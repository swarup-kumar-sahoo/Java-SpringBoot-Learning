
public class Assignment_Operator{

    public static void main(String[] args) {
        
        int a = 18;
        int b = 13;

        //Assignment operator

        //Add
        int sum  = a + b;
        System.out.println("Sum of a and b is: " + sum);

        //Subtract
        int sub = a - b;
        System.out.println("Subtraction of a and b is: " + sub);

        //Multiply
        int mul = a * b;
        System.out.println("Multiplying a and b is: " + mul);

        //Divide
        int div = a / b;
        System.out.println("Dividing a and b is :" + div);

         //Modulus
         int mod = a % b;
         System.out.println("Reminder a and b is :" + mod);
        

    }
}