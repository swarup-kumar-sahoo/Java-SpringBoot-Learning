public class Loops {
    public static void main(String[] args) {
        //while
        int i = 0;

        while (i <= 5) {

            System.err.println(i);
            i++;
        }

        //do-while
        int j = 0;

        do{
            System.err.println(j);
            j++;
        }while(j < 5);

        //For loop
        
        for(int a = 0;a < 12; a++){
            System.err.println(a);
            a++;
        }

        
    }
}
